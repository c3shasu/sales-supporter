package jp.co.ccube.ss.form.sample;

/**
 * 画面のコントロールに紐づくBeanクラス。
 * 
 * @author yuki
 *
 */
public class InputForm {

	/** パラメータ */
	private String param;

	/**
	 * パラメータを取得する。
	 * 
	 * @return パラメータ
	 */
	public String getParam() {
		return param;
	}

	/**
	 * パラメータを設定する。
	 * 
	 * @param param パラメータ
	 */
	public void setParam(String param) {
		this.param = param;
	}

}
